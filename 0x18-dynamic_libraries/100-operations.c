#include <stdio.h>

/**
*add- adding 2 int
*@a:1st int
*@b:2nd int
*Return: Sum
*/
int add(int a, int b)
{
	return a + b;
}

/**
*add- subtracting 2 int
*@a:1st int
*@b:2nd int
*Return: Difference
*/
int sub(int a, int b)
{
        return a - b;
}

/**
*add- multiplying 2 int
*@a:1st int
*@b:2nd int
*Return: Product
*/
int mul(int a, int b)
{
        return a * b;
}

/**
*div- dividing 2 int
*@a:1st int
*@b:2nd int
*Return: dividend 
*/
int div(int a, int b)
{
        return a / b;
}

/**
*add- module a in b
*@a:1st int
*@b:2nd int
*Return: module
*/
int mod(int a, int b)
{
        return a % b;
}
